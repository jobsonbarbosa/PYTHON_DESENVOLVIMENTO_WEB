sub = num1 - num2
div = num1 / num2
mult = num1 * num2 
mod = num1 % num2
exp = num1 ** num2

print(f"Soma = {sum}")
print(f"Subtração = {sub}")
print(f"Divição = {div}")
print(f"Multiplicação = {mult}")
print(f"Mode = {mod}")
print(f"Exponenciação = {exp}")

